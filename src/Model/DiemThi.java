package Model;

public class DiemThi {
    private String cccd;
    private String diemToan;
    private String diemVan;
    private String diemLy;

    private String diemHoa;
    private String diemSinh;
    private String diemSu;
    private String diemDia;
    private String diemGdcd;
    private String diemAnh;

    public DiemThi() {
    }

    public DiemThi(String cccd, String diemToan, String diemVan, String diemLy, String diemHoa, String diemSinh,
                   String diemSu, String diemDia, String diemGdcd, String diemAnh) {
        this.cccd = cccd;
        this.diemToan = diemToan;
        this.diemVan = diemVan;
        this.diemLy = diemLy;
        this.diemHoa = diemHoa;
        this.diemSinh = diemSinh;
        this.diemSu = diemSu;
        this.diemDia = diemDia;
        this.diemGdcd = diemGdcd;
        this.diemAnh = diemAnh;
    }

    public void setCccd(String cccd) {
        this.cccd = cccd;
    }

    public void setDiemToan(String diemToan) {
        this.diemToan = diemToan;
    }

    public void setDiemVan(String diemVan) {
        this.diemVan = diemVan;
    }

    public void setDiemLy(String diemLy) {
        this.diemLy = diemLy;
    }

    public void setDiemHoa(String diemHoa) {
        this.diemHoa = diemHoa;
    }

    public void setDiemSinh(String diemSinh) {
        this.diemSinh = diemSinh;
    }

    public void setDiemSu(String diemSu) {
        this.diemSu = diemSu;
    }

    public void setDiemDia(String diemDia) {
        this.diemDia = diemDia;
    }

    public void setDiemGdcd(String diemGdcd) {
        this.diemGdcd = diemGdcd;
    }

    public void setDiemAnh(String diemAnh) {
        this.diemAnh = diemAnh;
    }

    public String getCccd() {
        return cccd;
    }

    public String getDiemToan() {
        return diemToan;
    }

    public String getDiemVan() {
        return diemVan;
    }

    public String getDiemLy() {
        return diemLy;
    }

    public String getDiemHoa() {
        return diemHoa;
    }

    public String getDiemSinh() {
        return diemSinh;
    }

    public String getDiemSu() {
        return diemSu;
    }

    public String getDiemDia() {
        return diemDia;
    }

    public String getDiemGdcd() {
        return diemGdcd;
    }

    public String getDiemAnh() {
        return diemAnh;
    }
}
